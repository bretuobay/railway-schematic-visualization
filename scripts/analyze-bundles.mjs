import { gzipSync } from 'node:zlib';
import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';

const workspaceRoot = process.cwd();

const sizeTargets = [
  {
    category: 'core-main',
    path: 'dist/index.js',
    maxGzipBytes: 80 * 1024,
    summary: 'Primary core bundle must stay under 80 KB gzipped.',
  },
  {
    category: 'core-tree-shaken',
    path: 'dist/renderer/index.js',
    maxGzipBytes: 50 * 1024,
    summary: 'Largest granular core entry must stay under 50 KB gzipped.',
  },
  {
    category: 'core-tree-shaken',
    path: 'dist/coordinates/index.js',
    maxGzipBytes: 50 * 1024,
    summary: 'Granular coordinate entry must stay under 50 KB gzipped.',
  },
  {
    category: 'canvas-optional',
    path: 'packages/canvas/dist/lazy.js',
    maxGzipBytes: 10 * 1024,
    summary: 'Canvas lazy entry should remain a small loader chunk.',
  },
  {
    category: 'regional-optional',
    path: 'packages/adapters-regional/dist/lazy.js',
    maxGzipBytes: 10 * 1024,
    summary: 'Regional adapter lazy entry should remain a small loader chunk.',
  },
];

const packageContracts = [
  {
    path: 'package.json',
    checks: [
      (data) =>
        data.sideEffects === false
          ? undefined
          : 'Root package must declare sideEffects: false.',
      (data) =>
        data.peerDependencies?.d3 === '^7.9.0'
          ? undefined
          : 'Root package must declare d3 as a peer dependency.',
      (data) =>
        data.dependencies?.d3 === undefined
          ? undefined
          : 'Root package must not bundle d3 as a direct dependency.',
    ],
  },
  {
    path: 'packages/canvas/package.json',
    checks: [
      (data) =>
        data.sideEffects === false
          ? undefined
          : 'Canvas package must declare sideEffects: false.',
      (data) =>
        data.exports?.['./lazy']
          ? undefined
          : 'Canvas package must expose a ./lazy subpath export.',
    ],
  },
  {
    path: 'packages/adapters-regional/package.json',
    checks: [
      (data) =>
        data.sideEffects === false
          ? undefined
          : 'Regional adapter package must declare sideEffects: false.',
      (data) =>
        data.exports?.['./lazy']
          ? undefined
          : 'Regional adapter package must expose a ./lazy subpath export.',
    ],
  },
];

const measurements = sizeTargets.map((target) => {
  const absolutePath = resolve(workspaceRoot, target.path);
  const content = readFileSync(absolutePath);
  const gzipBytes = gzipSync(content).byteLength;

  return {
    ...target,
    gzipBytes,
    rawBytes: content.byteLength,
  };
});

const contractFailures = [];

for (const contract of packageContracts) {
  const absolutePath = resolve(workspaceRoot, contract.path);
  const data = JSON.parse(readFileSync(absolutePath, 'utf8'));

  for (const check of contract.checks) {
    const failure = check(data);

    if (failure) {
      contractFailures.push(failure);
    }
  }
}

const sizeFailures = measurements
  .filter((measurement) => measurement.gzipBytes > measurement.maxGzipBytes)
  .map(
    (measurement) =>
      `${measurement.path} exceeds its gzip budget (${formatBytes(
        measurement.gzipBytes,
      )} > ${formatBytes(measurement.maxGzipBytes)}).`,
  );

const report = createReport(measurements);
const reportPath = resolve(workspaceRoot, 'docs/bundle-size.md');

mkdirSync(dirname(reportPath), { recursive: true });
writeFileSync(reportPath, report, 'utf8');

console.log('Bundle size report written to docs/bundle-size.md');
measurements.forEach((measurement) => {
  console.log(
    `${measurement.path}: ${formatBytes(measurement.gzipBytes)} gzip (${formatBytes(
      measurement.rawBytes,
    )} raw)`,
  );
});

const failures = [...sizeFailures, ...contractFailures];

if (failures.length > 0) {
  failures.forEach((failure) => {
    console.error(failure);
  });
  process.exitCode = 1;
}

function formatBytes(value) {
  const kib = value / 1024;

  return `${kib.toFixed(2)} KiB`;
}

function createReport(entries) {
  const lines = [
    '# Bundle Size Analysis',
    '',
    'This report is generated by `npm run analyze:bundles` and documents the tree-shakeable package boundaries and current gzip budgets.',
    '',
    '## Tree-Shakeable Imports',
    '',
    '- `@rail-schematic-viz/core` root bundle for full core usage.',
    '- `@rail-schematic-viz/core/coordinates` and `@rail-schematic-viz/core/renderer` for granular imports.',
    '- `@rail-schematic-viz/canvas/lazy` for on-demand Canvas renderer loading.',
    '- `@rail-schematic-viz/adapters-regional/lazy` for on-demand regional adapter loading.',
    '',
    '## Measurements',
    '',
    '| Target | File | Gzip | Raw | Budget | Status |',
    '| --- | --- | ---: | ---: | ---: | --- |',
  ];

  for (const entry of entries) {
    lines.push(
      `| ${entry.category} | \`${entry.path}\` | ${formatBytes(entry.gzipBytes)} | ${formatBytes(
        entry.rawBytes,
      )} | ${formatBytes(entry.maxGzipBytes)} | ${
        entry.gzipBytes <= entry.maxGzipBytes ? 'Pass' : 'Fail'
      } |`,
    );
  }

  lines.push(
    '',
    '## Notes',
    '',
    '- Core package exports remain side-effect free to preserve tree-shaking.',
    '- Optional packages stay in separate workspaces and are loaded through dedicated lazy entrypoints.',
    '- `d3` is declared as a peer dependency at the core boundary to avoid duplicate bundling.',
  );

  return `${lines.join('\n')}\n`;
}
